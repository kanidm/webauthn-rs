    #[test]
    fn response_attrs() {
        // OK
        let mut r = ISO7816ResponseAPDU {
            sw1: 0x90,
            sw2: 0x00,
            data: vec![],
        };
        assert!(r.is_ok());
        assert!(r.is_success());
        assert!(!r.ctap_needs_get_response());
        assert_eq!(0, r.bytes_available());

        // More bytes available
        r = ISO7816ResponseAPDU {
            sw1: 0x61,
            sw2: 0x01,
            data: vec![],
        };
        assert!(!r.is_ok());
        assert!(r.is_success());
        assert!(!r.ctap_needs_get_response());
        assert_eq!(1, r.bytes_available());

        r = ISO7816ResponseAPDU {
            sw1: 0x61,
            sw2: 0x00,
            data: vec![],
        };
        assert!(!r.is_ok());
        assert!(r.is_success());
        assert!(!r.ctap_needs_get_response());
        assert_eq!(256, r.bytes_available());

        // Needs NFCCTAP_GETRESPONSE
        r = ISO7816ResponseAPDU {
            sw1: 0x91,
            sw2: 0x00,
            data: vec![],
        };
        assert!(!r.is_ok());
        assert!(r.is_success());
        assert!(r.ctap_needs_get_response());
        assert_eq!(0, r.bytes_available());

        // Error
        r = ISO7816ResponseAPDU {
            sw1: 0x6A,
            sw2: 0x82,
            data: vec![],
        };
        assert!(!r.is_ok());
        assert!(!r.is_success());
        assert!(!r.ctap_needs_get_response());
        assert_eq!(0, r.bytes_available());
    }

